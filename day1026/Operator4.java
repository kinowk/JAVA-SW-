package day1026;
/*
관계연산자 >, <, >=, =<, ==, !=
*/
class Operator4 {
	public static void main(String[] args) {
		int i = 10, j = 26, k = 10;
		System.out.println(i+" == " + k + " = " + (i == k) );
		System.out.println(i+" == " + j + " = " + (i == j) );

		System.out.println(i+" != " + k + " = " + (i != k) );
		System.out.println(i+" != " + j + " = " + (i != j) );
	}
}
